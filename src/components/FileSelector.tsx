import React, { useState, useEffect, useCallback } from 'react';
import './FileSelector.css';

interface FileItem {
  name: string;
  path: string;
  type: 'file' | 'directory';
  size?: number;
  modified?: number;
  created?: number;
  children_count?: number;
  file_count?: number;
  dir_count?: number;
  extension?: string;
  icon?: string;
  category?: string;
  is_readable?: boolean;
  is_writable?: boolean;
}

interface DirectoryData {
  type: 'directory';
  name: string;
  path: string;
  parent_path: string | null;
  items: FileItem[];
  breadcrumbs: Array<{ name: string; path: string }>;
  total_items: number;
  total_files: number;
  total_directories: number;
  base_path: string;
  current_time: string;
}

interface FileSelectorProps {
  isOpen: boolean;
  onClose: () => void;
  onSelect: (path: string, type: 'file' | 'directory') => void;
  mode: 'file' | 'directory' | 'both';
  title?: string;
  allowedExtensions?: string[];
}

const FileSelector: React.FC<FileSelectorProps> = ({
  isOpen,
  onClose,
  onSelect,
  mode = 'both',
  title = 'ÈÄâÊã©Êñá‰ª∂',
  allowedExtensions = [],
}) => {
  const [currentPath, setCurrentPath] = useState<string>('');
  const [items, setItems] = useState<FileItem[]>([]);
  const [loading, setLoading] = useState<boolean>(false);
  const [error, setError] = useState<string>('');
  const [selectedItem, setSelectedItem] = useState<FileItem | null>(null);
  const [parentPath, setParentPath] = useState<string | null>(null);
  const [breadcrumbs, setBreadcrumbs] = useState<
    Array<{ name: string; path: string }>
  >([]);
  const [directoryInfo, setDirectoryInfo] = useState<{
    name: string;
    total_files: number;
    total_directories: number;
  } | null>(null);

  // Ëé∑ÂèñÁõÆÂΩïÂÜÖÂÆπ
  const fetchDirectoryContents = useCallback(
    async (path: string = '') => {
      setLoading(true);
      setError('');

      try {
        const showFiles = mode === 'file' || mode === 'both';
        let url = `/api/rei/filesystem/browse?path=${encodeURIComponent(
          path
        )}&show_files=${showFiles}`;

        // Ê∑ªÂä†Êñá‰ª∂Á±ªÂûãËøáÊª§
        if (allowedExtensions.length > 0) {
          url += `&file_types=${allowedExtensions.join(',')}`;
        }

        const response = await fetch(url);

        if (!response.ok) {
          const errorData = await response.json();
          throw new Error(errorData.error || 'Ëé∑ÂèñÁõÆÂΩïÂÜÖÂÆπÂ§±Ë¥•');
        }

        const data: DirectoryData = await response.json();

        if (data.type !== 'directory') {
          // Â¶ÇÊûúËÆøÈóÆÁöÑÊòØÊñá‰ª∂ÔºåËøîÂõûÂÖ∂Áà∂ÁõÆÂΩï
          const parentDir = path.split('/').slice(0, -1).join('/');
          await fetchDirectoryContents(parentDir);
          return;
        }

        setItems(data.items);
        setCurrentPath(data.path);
        setParentPath(data.parent_path);
        setBreadcrumbs(data.breadcrumbs || []);
        setDirectoryInfo({
          name: data.name,
          total_files: data.total_files,
          total_directories: data.total_directories,
        });
        setSelectedItem(null);
      } catch (err) {
        console.error('Ëé∑ÂèñÁõÆÂΩïÂÜÖÂÆπÂ§±Ë¥•:', err);
        setError(err instanceof Error ? err.message : 'Ëé∑ÂèñÁõÆÂΩïÂÜÖÂÆπÂ§±Ë¥•');
      } finally {
        setLoading(false);
      }
    },
    [mode, allowedExtensions]
  );

  // ÁªÑ‰ª∂ÊåÇËΩΩÊó∂Âä†ËΩΩÊ†πÁõÆÂΩï
  useEffect(() => {
    if (isOpen) {
      fetchDirectoryContents('');
    }
  }, [isOpen, fetchDirectoryContents]);

  // Â§ÑÁêÜÂèåÂáª‰∫ã‰ª∂
  const handleDoubleClick = (item: FileItem) => {
    if (item.type === 'directory') {
      fetchDirectoryContents(item.path);
    } else if (mode === 'file' || mode === 'both') {
      onSelect(item.path, item.type);
      onClose();
    }
  };

  // Â§ÑÁêÜÂçïÂáªÈÄâÊã©
  const handleItemSelect = (item: FileItem) => {
    setSelectedItem(item);
  };

  // Â§ÑÁêÜËøîÂõû‰∏äÁ∫ßÁõÆÂΩï
  const handleGoBack = () => {
    fetchDirectoryContents(parentPath || '');
  };

  // Â§ÑÁêÜÁ°ÆËÆ§ÈÄâÊã©
  const handleConfirm = () => {
    if (!selectedItem) return;

    // Ê£ÄÊü•Ê®°ÂºèÂåπÈÖç
    if (mode === 'file' && selectedItem.type !== 'file') return;
    if (mode === 'directory' && selectedItem.type !== 'directory') return;

    onSelect(selectedItem.path, selectedItem.type);
    onClose();
  };

  // Ê†ºÂºèÂåñÊñá‰ª∂Â§ßÂ∞è
  const formatFileSize = (bytes: number): string => {
    if (bytes === 0) return '0 B';
    const k = 1024;
    const sizes = ['B', 'KB', 'MB', 'GB'];
    const i = Math.floor(Math.log(bytes) / Math.log(k));
    return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
  };

  // Ê†ºÂºèÂåñ‰øÆÊîπÊó∂Èó¥
  const formatModifiedTime = (timestamp: number): string => {
    return new Date(timestamp * 1000).toLocaleString('zh-CN');
  };

  // Ëé∑ÂèñÊñá‰ª∂ÂõæÊ†á
  const getFileIcon = (item: FileItem): string => {
    if (item.type === 'directory') {
      return 'üìÅ';
    }

    // ‰ºòÂÖà‰ΩøÁî®ÂêéÁ´ØÊèê‰æõÁöÑÂõæÊ†á
    if (item.icon) {
      return item.icon;
    }

    // ÂêéÂ§áÊñπÊ°àÔºöÊ†πÊçÆÊâ©Â±ïÂêçÂà§Êñ≠
    const extension =
      item.extension || item.name.split('.').pop()?.toLowerCase();
    switch (extension) {
      case 'png':
      case 'jpg':
      case 'jpeg':
      case 'gif':
      case 'webp':
        return 'üñºÔ∏è';
      case 'txt':
      case 'md':
        return 'üìÑ';
      case 'json':
        return 'üìã';
      case 'py':
        return 'üêç';
      case 'js':
      case 'ts':
        return 'üìú';
      default:
        return 'üìÑ';
    }
  };

  // Â§ÑÁêÜÈù¢ÂåÖÂ±ëÁÇπÂáª
  const handleBreadcrumbClick = (path: string) => {
    fetchDirectoryContents(path);
  };

  if (!isOpen) return null;

  return (
    <div className="file-selector-overlay">
      <div className="file-selector-modal">
        <div className="file-selector-header">
          <h3>{title}</h3>
          <button className="close-button" onClick={onClose}>
            √ó
          </button>
        </div>

        <div className="file-selector-toolbar">
          <div className="breadcrumb-nav">
            <button
              className="breadcrumb-item root"
              onClick={() => handleBreadcrumbClick('')}
              disabled={loading}
            >
              üè† {directoryInfo?.name || 'ComfyUI'}
            </button>
            {breadcrumbs.map((crumb, index) => (
              <React.Fragment key={crumb.path}>
                <span className="breadcrumb-separator">/</span>
                <button
                  className="breadcrumb-item"
                  onClick={() => handleBreadcrumbClick(crumb.path)}
                  disabled={loading}
                >
                  {crumb.name}
                </button>
              </React.Fragment>
            ))}
          </div>

          <div className="toolbar-actions">
            {parentPath !== null && (
              <button
                className="back-button"
                onClick={handleGoBack}
                disabled={loading}
              >
                ‚¨ÜÔ∏è ËøîÂõû‰∏äÁ∫ß
              </button>
            )}
          </div>
        </div>

        {directoryInfo && (
          <div className="directory-info">
            <span className="info-item">
              üìÇ {directoryInfo.total_directories} ‰∏™Êñá‰ª∂Â§π
            </span>
            <span className="info-item">
              üìÑ {directoryInfo.total_files} ‰∏™Êñá‰ª∂
            </span>
          </div>
        )}

        <div className="file-selector-content">
          {loading && <div className="loading-message">Âä†ËΩΩ‰∏≠...</div>}

          {error && <div className="error-message">ÈîôËØØ: {error}</div>}

          {!loading && !error && (
            <div className="file-list">
              {items.length === 0 ? (
                <div className="empty-message">
                  Ê≠§ÁõÆÂΩï‰∏∫Á©∫ÊàñÊ≤°ÊúâÁ¨¶ÂêàÊù°‰ª∂ÁöÑÊñá‰ª∂
                </div>
              ) : (
                items.map((item) => (
                  <div
                    key={item.path}
                    className={`file-item ${
                      selectedItem?.path === item.path ? 'selected' : ''
                    }`}
                    onClick={() => handleItemSelect(item)}
                    onDoubleClick={() => handleDoubleClick(item)}
                  >
                    <div className="file-item-icon">{getFileIcon(item)}</div>
                    <div className="file-item-info">
                      <div className="file-item-name">{item.name}</div>
                      <div className="file-item-details">
                        {item.type === 'directory' ? (
                          <span>
                            {item.children_count || 0} È°π
                            {item.file_count !== undefined &&
                              item.dir_count !== undefined && (
                                <span className="detailed-count">
                                  {' '}
                                  ({item.dir_count} Êñá‰ª∂Â§π, {item.file_count}{' '}
                                  Êñá‰ª∂)
                                </span>
                              )}
                          </span>
                        ) : (
                          <>
                            <span>{formatFileSize(item.size || 0)}</span>
                            {item.extension && (
                              <span className="file-extension">
                                .{item.extension}
                              </span>
                            )}
                            {item.modified && (
                              <span>
                                {' '}
                                ‚Ä¢ {formatModifiedTime(item.modified)}
                              </span>
                            )}
                          </>
                        )}
                      </div>
                      {!item.is_readable && (
                        <div className="file-warning">‚ö†Ô∏è Êó†ËØªÂèñÊùÉÈôê</div>
                      )}
                    </div>
                  </div>
                ))
              )}
            </div>
          )}
        </div>

        <div className="file-selector-footer">
          <div className="selected-info">
            {selectedItem && (
              <span>
                Â∑≤ÈÄâÊã©: {selectedItem.name} (
                {selectedItem.type === 'directory' ? 'Êñá‰ª∂Â§π' : 'Êñá‰ª∂'})
              </span>
            )}
          </div>

          <div className="action-buttons">
            <button className="cancel-button" onClick={onClose}>
              ÂèñÊ∂à
            </button>
            <button
              className="confirm-button"
              onClick={handleConfirm}
              disabled={
                !selectedItem ||
                (mode === 'file' && selectedItem.type !== 'file') ||
                (mode === 'directory' && selectedItem.type !== 'directory')
              }
            >
              Á°ÆËÆ§ÈÄâÊã©
            </button>
          </div>
        </div>
      </div>
    </div>
  );
};

export default FileSelector;
